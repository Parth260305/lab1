def add(a,b):
    return a+b
def sub(a,b):
    return a-b
def mul(a,b):
    return a*b
def div(a,b):
    if b!=0:
        return a/b
    else:
        return "Zero Division is not allow"
def perc(a,b):
    if b !=0:
        return(a/b)*100
    else:
        return "Total is zero"
    
